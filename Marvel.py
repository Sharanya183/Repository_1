#Avengers are Evergreen#

Name = input(str("Enter your name: "))
if Name == "Thanos":
    print("Get out, loser!")
elif Name == "Peter Parker":
    print("What up, Spider-man?")
elif Name == "Tony Stark":
    print("Hello Iron Man")
elif Name == "Steve Rogers":
    print("Hey Captain!")
elif Name == "Natasha Romanoff":
    print("Welcome Black Widow")
elif Name == "Bruce Banner":
    print("The Hulk is here!")
elif Name == "Stephen Strange":
    print("There's the wizard!")
elif Name == "Wanda Maximoff":
    print("Scarlet's here! Just kidding. Welcome Scarlet Witch")
else:
    print("Not the original dude.")
